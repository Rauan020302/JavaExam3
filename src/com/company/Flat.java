package com.company;

public abstract class Flat extends Backpack{
    private String name;

    public Flat(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
